#!/usr/bin/env python3
"""Build a paper-ready *Literature Reproduction Summary* from the
literature_faithfulness JSON.

Why this exists
---------------
Reviewers ask "which figure / section / table is each of your numbers
reproducing?". The lit-faithfulness comparator already records the
expected sign, observed Δ, and a citation string per claim — this
script groups them by **citation** so the reader sees one row per
published claim with all corpus cells we ran for it.

Output
------
A grouped markdown table:

    ### Faldu et al. HPCA 2020 Fig 10  (PR, GRASP)
    | graph | L3 | expected_sign | observed Δ vs LRU | tolerance | verdict |
    | --- | --- | --- | --- | --- | --- |
    | web-Google | 1MB | - | -14.766pp | ±0.5pp | ✅ |
    ...

Usage
-----
    python -m scripts.experiments.ecg.literature_reproduction_summary \\
        --lit-faith-json wiki/data/literature_faithfulness_postfix.json \\
        --markdown wiki/data/literature_reproduction_summary.md \\
        --csv wiki/data/literature_reproduction_summary.csv
"""

from __future__ import annotations

import argparse
import csv
import json
import sys
from collections import defaultdict
from pathlib import Path
from typing import Iterable

REPO_ROOT = Path(__file__).resolve().parents[3]

# Citation ordering: list paper-rooted citations first, then derived /
# extended ones.
CITATION_ORDER_PREFIX = (
    "Faldu et al. HPCA 2020 Fig",
    "Faldu et al. HPCA 2020 §",
    "Faldu HPCA20",
    "Balaji & Lucia HPCA 2021 Fig",
    "Balaji & Lucia HPCA 2021 §",
    "Jaleel et al. ISCA 2010",
)


def _verdict_glyph(status: str) -> str:
    return {
        "ok": "✅",
        "within_tolerance": "🟡",
        "known_deviation": "📘",
        "disagree": "⛔",
        "missing": "⏳",
        "insufficient_data": "🔬",
    }.get(status, status)


def _format_delta(entry: dict) -> str:
    d = entry.get("delta_pct")
    if d is None:
        return "—"
    return f"{d:+.3f}pp"


def _expected_window(entry: dict) -> str:
    """Render the expected-tolerance window for a row."""
    mn = entry.get("min_abs_delta_pct")
    mx = entry.get("max_abs_delta_pct")
    tol = entry.get("tolerance_pct")
    sign = entry.get("expected_sign", "")
    parts = []
    if sign:
        parts.append(f"sign={sign}")
    if mn is not None and mx is not None:
        parts.append(f"|Δ|∈[{mn:.2f},{mx:.2f}]pp")
    elif mx is not None:
        parts.append(f"|Δ|≤{mx:.2f}pp")
    elif mn is not None:
        parts.append(f"|Δ|≥{mn:.2f}pp")
    if tol is not None:
        parts.append(f"±{tol:.2f}pp")
    return ", ".join(parts) or "—"


def _key(citation: str) -> tuple[int, str]:
    for i, prefix in enumerate(CITATION_ORDER_PREFIX):
        if citation.startswith(prefix):
            return (i, citation)
    return (len(CITATION_ORDER_PREFIX), citation)


def render_markdown(per_claim: list[dict]) -> str:
    grouped: dict[str, list[dict]] = defaultdict(list)
    for e in per_claim:
        grouped[e["citation"]].append(e)

    out: list[str] = [
        "# Literature reproduction summary",
        "",
        "_Generated by `scripts/experiments/ecg/literature_reproduction_summary.py`._",
        "",
        "Each section groups cells that reproduce a single published claim.",
        "Verdict legend: ✅ ok · 🟡 within_tolerance · 📘 known_deviation"
        " · ⛔ disagree · ⏳ missing · 🔬 insufficient_data.",
        "",
    ]

    totals_per_citation: list[tuple[str, int, int, int, int, int, int]] = []

    for citation in sorted(grouped, key=_key):
        rows = grouped[citation]
        ok = sum(1 for e in rows if e["status"] == "ok")
        wt = sum(1 for e in rows if e["status"] == "within_tolerance")
        kd = sum(1 for e in rows if e["status"] == "known_deviation")
        dg = sum(1 for e in rows if e["status"] == "disagree")
        ms = sum(1 for e in rows if e["status"] == "missing")
        ins = sum(1 for e in rows if e["status"] == "insufficient_data")
        totals_per_citation.append((citation, ok, wt, kd, dg, ms, ins))

        out.append(f"## {citation}")
        out.append("")
        out.append(
            f"_{len(rows)} cell(s):_ {ok} ok · {wt} within_tolerance"
            f" · {kd} known_deviation · {dg} disagree · {ms} missing"
            f" · {ins} insufficient_data."
        )
        out.append("")
        out.append("| graph | app | L3 | policy | expected | observed Δ | verdict |")
        out.append("|---|---|---|---|---|---|:---:|")
        for e in sorted(rows, key=lambda r: (r["graph"], r["app"], r["l3_size"], r["policy"])):
            out.append(
                f"| {e['graph']} | {e['app']} | {e['l3_size']} | {e['policy']} | "
                f"{_expected_window(e)} | {_format_delta(e)} | {_verdict_glyph(e['status'])} |"
            )
        out.append("")

    # Roll-up table at top (insert after legend by rewriting list).
    rollup = [
        "## Per-citation roll-up",
        "",
        "| citation | cells | ok | within_tol | known_dev | disagree | missing | insufficient |",
        "|---|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for citation, ok, wt, kd, dg, ms, ins in totals_per_citation:
        total = ok + wt + kd + dg + ms + ins
        rollup.append(
            f"| {citation} | {total} | {ok} | {wt} | {kd} | {dg} | {ms} | {ins} |"
        )
    rollup.append("")
    # Insert rollup after the legend (index 6 — right after the blank line
    # following the legend).
    out = out[:7] + rollup + out[7:]
    return "\n".join(out) + "\n"


def render_csv(per_claim: list[dict]) -> str:
    fields = [
        "citation", "graph", "app", "l3_size", "policy",
        "expected_sign", "min_abs_delta_pct", "max_abs_delta_pct",
        "tolerance_pct", "delta_pct", "status",
    ]
    import io
    buf = io.StringIO()
    w = csv.DictWriter(buf, fieldnames=fields, extrasaction="ignore")
    w.writeheader()
    for e in per_claim:
        w.writerow({f: e.get(f) for f in fields})
    return buf.getvalue()


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument(
        "--lit-faith-json",
        default=str(REPO_ROOT / "wiki" / "data" / "literature_faithfulness_postfix.json"),
        help="Path to literature_faithfulness JSON output.",
    )
    parser.add_argument(
        "--markdown",
        default=str(REPO_ROOT / "wiki" / "data" / "literature_reproduction_summary.md"),
        help="Path to write the rendered markdown.",
    )
    parser.add_argument(
        "--csv",
        default=str(REPO_ROOT / "wiki" / "data" / "literature_reproduction_summary.csv"),
        help="Path to write the per-cell CSV.",
    )
    args = parser.parse_args()

    raw = json.loads(Path(args.lit_faith_json).read_text())
    per_claim = raw.get("per_claim", [])
    if not per_claim:
        print("[lit-reproduction] no per_claim entries in JSON", file=sys.stderr)
        return 2

    md = render_markdown(per_claim)
    Path(args.markdown).write_text(md)
    print(f"[lit-reproduction] markdown -> {args.markdown}", file=sys.stderr)

    Path(args.csv).write_text(render_csv(per_claim))
    print(f"[lit-reproduction] csv -> {args.csv}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    sys.exit(main())
