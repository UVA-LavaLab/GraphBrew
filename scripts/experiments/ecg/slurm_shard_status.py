#!/usr/bin/env python3
"""Summarize ECG final-run Slurm shard status without launching simulations."""

from __future__ import annotations

import argparse
from collections import Counter
import csv
import json
from pathlib import Path
import sys
from typing import Any

import final_paper_run
from make_slurm_shards import ShardRow


DEFAULT_RUNS_ROOT = final_paper_run.PROJECT_ROOT / "results" / "ecg_experiments" / "final_paper_runs" / "slurm"
STATUS_FIELDNAMES = [
    "profile",
    "stage",
    "graph",
    "benchmark",
    "policy",
    "run_tag",
    "run_dir",
    "status",
    "detail",
    "job_status_counts",
    "combined_roi_rows",
    "combined_proof_rows",
]


def read_shards(path: Path) -> list[ShardRow]:
    rows: list[ShardRow] = []
    for line_number, line in enumerate(path.read_text().splitlines(), 1):
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        parts = line.split("\t")
        if len(parts) != 6:
            raise SystemExit(f"invalid shard row {path}:{line_number}: expected 6 tab-separated fields")
        rows.append(ShardRow(*parts))
    return rows


def shard_run_dir(row: ShardRow, runs_root: Path) -> Path:
    safe_policy = row.policy.replace(":", "_")
    return runs_root / row.run_tag / f"{row.profile}_{row.graph}_{row.benchmark}_{safe_policy}"


def format_counts(values: Counter[str]) -> str:
    return ";".join(f"{key}:{values[key]}" for key in sorted(values))


def count_csv_rows(path: Path) -> int:
    if not path.exists():
        return 0
    with path.open(newline="") as fh:
        return len(list(csv.DictReader(fh)))


def run_has_open_start(run_dir: Path) -> bool:
    status_path = run_dir / "run_status.jsonl"
    if not status_path.exists():
        return False
    last_by_job: dict[str, dict[str, Any]] = {}
    for line in status_path.read_text().splitlines():
        if not line.strip():
            continue
        try:
            record = json.loads(line)
        except json.JSONDecodeError:
            continue
        job_id = str(record.get("job_id", ""))
        if job_id:
            last_by_job[job_id] = record
    return any(record.get("event") == "start" for record in last_by_job.values())


def summarize_shard(row: ShardRow, runs_root: Path) -> dict[str, Any]:
    run_dir = shard_run_dir(row, runs_root)
    record: dict[str, Any] = {
        "profile": row.profile,
        "stage": row.stage,
        "graph": row.graph,
        "benchmark": row.benchmark,
        "policy": row.policy,
        "run_tag": row.run_tag,
        "run_dir": str(run_dir),
        "status": "not_started",
        "detail": "run directory missing",
        "job_status_counts": "",
        "combined_roi_rows": 0,
        "combined_proof_rows": 0,
    }
    if not run_dir.exists():
        return record

    record["combined_roi_rows"] = count_csv_rows(run_dir / "combined_roi_matrix.csv")
    record["combined_proof_rows"] = count_csv_rows(run_dir / "combined_proof_matrix.csv")
    jobs_path = run_dir / "jobs.csv"
    if not jobs_path.exists():
        record["status"] = "running" if run_has_open_start(run_dir) else "pending"
        record["detail"] = "jobs.csv missing"
        return record

    with jobs_path.open(newline="") as fh:
        job_rows = list(csv.DictReader(fh))
    if not job_rows:
        record["status"] = "pending"
        record["detail"] = "jobs.csv has no rows"
        return record

    counts = Counter(row.get("status", "") or "unknown" for row in job_rows)
    record["job_status_counts"] = format_counts(counts)
    if set(counts) == {"ok"}:
        record["status"] = "ok"
        record["detail"] = f"{sum(counts.values())} ok job(s)"
    elif "failed" in counts or "error" in counts:
        record["status"] = "failed"
        record["detail"] = record["job_status_counts"]
    elif run_has_open_start(run_dir):
        record["status"] = "running"
        record["detail"] = record["job_status_counts"]
    else:
        record["status"] = "pending"
        record["detail"] = record["job_status_counts"]
    return record


def summarize_shards(rows: list[ShardRow], runs_root: Path) -> list[dict[str, Any]]:
    return [summarize_shard(row, runs_root) for row in rows]


def write_status(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=STATUS_FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Summarize ECG Slurm shard status from a shard TSV.")
    parser.add_argument("--shards", required=True, help="Shard TSV generated by make_slurm_shards.py.")
    parser.add_argument("--runs-root", default=str(DEFAULT_RUNS_ROOT), help="Root containing <run_tag>/<shard-run-dir> directories.")
    parser.add_argument("--out", default="-", help="Output status CSV path, or '-' for stdout.")
    parser.add_argument("--fail-on-missing", action="store_true", help="Exit nonzero if any shard is not started or pending.")
    parser.add_argument("--fail-on-failed", action="store_true", help="Exit nonzero if any shard failed.")
    parser.add_argument("--require-ok", action="store_true", help="Exit nonzero unless every shard status is ok; useful before promoting a smoke shard to a full array.")
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    shard_path = final_paper_run.resolve_path(str(args.shards))
    runs_root = final_paper_run.resolve_path(str(args.runs_root))
    rows = summarize_shards(read_shards(shard_path), runs_root)
    counts = Counter(str(row["status"]) for row in rows)
    if args.out == "-":
        writer = csv.DictWriter(sys.stdout, fieldnames=STATUS_FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)
    else:
        out_path = final_paper_run.resolve_path(str(args.out))
        write_status(out_path, rows)
        print(f"[write] {out_path} rows={len(rows)} status_counts={dict(sorted(counts.items()))}")

    if args.fail_on_failed and counts.get("failed", 0):
        return 2
    if args.fail_on_missing and any(counts.get(status, 0) for status in ("not_started", "pending")):
        return 3
    if args.require_ok and (set(counts) != {"ok"}):
        return 4
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))