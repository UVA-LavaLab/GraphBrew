#!/usr/bin/env python3
"""Per-graph claim density mini-report.

For each graph in the corpus, count how many literature claims the
reproduction summary registered against it, broken down by status
(``ok`` / ``within_tolerance`` / ``known_deviation`` / etc.) and by
unique (app, l3_size) cell. This answers reviewer questions like:

* "Why does email-Eu-core only have 19 claims?"
* "Which graph contributes the most evidence to the GRASP claim?"
* "Where are the holes in our coverage?"

Output
------
* ``wiki/data/claim_density.csv``  — one row per graph.
* ``wiki/data/claim_density.json`` — same data, machine readable.
* ``wiki/data/claim_density.md``   — paper-ready markdown.
"""

from __future__ import annotations

import argparse
import csv
import json
from collections import Counter, defaultdict
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
WIKI = REPO_ROOT / "wiki" / "data"

DEFAULT_REPRO_CSV = WIKI / "literature_reproduction_summary.csv"
DEFAULT_CSV_OUT = WIKI / "claim_density.csv"
DEFAULT_JSON_OUT = WIKI / "claim_density.json"
DEFAULT_MD_OUT = WIKI / "claim_density.md"

# Status order for stable reporting.
STATUS_ORDER = ["ok", "within_tolerance", "known_deviation", "missing", "no_claim", "fail"]


def _per_graph(rows: list[dict]) -> list[dict]:
    by_graph: dict[str, list[dict]] = defaultdict(list)
    for r in rows:
        by_graph[r["graph"]].append(r)

    out: list[dict] = []
    for g, grows in sorted(by_graph.items()):
        statuses = Counter(r["status"] for r in grows)
        cells = {(r["app"], r["l3_size"]) for r in grows}
        apps = sorted({r["app"] for r in grows})
        policies = sorted({r["policy"] for r in grows})
        citations = sorted({r["citation"] for r in grows if r.get("citation")})
        n = len(grows)
        n_ok = statuses.get("ok", 0)
        out.append(
            {
                "graph": g,
                "n_claims": n,
                "n_ok": n_ok,
                "ok_pct": (n_ok / n * 100.0) if n else 0.0,
                "status_counts": dict(statuses),
                "n_cells": len(cells),
                "apps": apps,
                "n_apps": len(apps),
                "policies": policies,
                "n_policies": len(policies),
                "citations": citations,
                "n_citations": len(citations),
            }
        )
    return out


def _emit_csv(rows: list[dict], out: Path) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    cols = [
        "graph", "n_claims", "n_ok", "ok_pct", "n_cells",
        "n_apps", "n_policies", "n_citations", "apps", "policies",
    ]
    with out.open("w", newline="") as fh:
        w = csv.DictWriter(fh, fieldnames=cols)
        w.writeheader()
        for r in rows:
            line = {
                "graph": r["graph"],
                "n_claims": r["n_claims"],
                "n_ok": r["n_ok"],
                "ok_pct": f"{r['ok_pct']:.2f}",
                "n_cells": r["n_cells"],
                "n_apps": r["n_apps"],
                "n_policies": r["n_policies"],
                "n_citations": r["n_citations"],
                "apps": ";".join(r["apps"]),
                "policies": ";".join(r["policies"]),
            }
            w.writerow(line)


def _emit_json(rows: list[dict], summary: dict, out: Path) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(
        {"schema_version": 1, "summary": summary, "graphs": rows},
        indent=2, sort_keys=True) + "\n")


def _emit_md(rows: list[dict], summary: dict, out: Path) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    lines: list[str] = []
    lines.append("# Per-graph literature claim density\n")
    lines.append(
        "Generated by `scripts/experiments/ecg/claim_density_report.py`. "
        "For each corpus graph this report counts the literature claims "
        "registered in the reproduction summary and shows the per-graph "
        "status mix. Use to spot under-tested graphs or status drift "
        "concentrated on one part of the corpus.\n\n"
    )
    lines.append(
        f"* Graphs: **{summary['n_graphs']}**\n"
        f"* Total claims: **{summary['total_claims']}**\n"
        f"* Overall OK: **{summary['total_ok']}** "
        f"({summary['total_ok_pct']:.1f}%)\n\n"
    )
    lines.append("| graph | claims | OK | OK% | cells | apps | policies | citations |\n")
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|\n")
    for r in sorted(rows, key=lambda x: -x["n_claims"]):
        lines.append(
            f"| {r['graph']} | {r['n_claims']} | {r['n_ok']} | "
            f"{r['ok_pct']:.1f} | {r['n_cells']} | {r['n_apps']} | "
            f"{r['n_policies']} | {r['n_citations']} |\n"
        )

    lines.append("\n## Status mix per graph\n\n")
    all_statuses = sorted({
        s for r in rows for s in r["status_counts"].keys()
    }, key=lambda s: (STATUS_ORDER.index(s) if s in STATUS_ORDER else 99, s))
    header = "| graph | " + " | ".join(all_statuses) + " |\n"
    sep = "|---|" + "|".join(["---:"] * len(all_statuses)) + "|\n"
    lines.append(header)
    lines.append(sep)
    for r in sorted(rows, key=lambda x: x["graph"]):
        row = "| " + r["graph"] + " | " + " | ".join(
            str(r["status_counts"].get(s, 0)) for s in all_statuses
        ) + " |\n"
        lines.append(row)
    out.write_text("".join(lines))


def _summarise(rows: list[dict]) -> dict:
    total = sum(r["n_claims"] for r in rows)
    total_ok = sum(r["n_ok"] for r in rows)
    return {
        "n_graphs": len(rows),
        "total_claims": total,
        "total_ok": total_ok,
        "total_ok_pct": (total_ok / total * 100.0) if total else 0.0,
    }


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--repro-csv", default=str(DEFAULT_REPRO_CSV))
    p.add_argument("--csv-out", default=str(DEFAULT_CSV_OUT))
    p.add_argument("--json-out", default=str(DEFAULT_JSON_OUT))
    p.add_argument("--md-out", default=str(DEFAULT_MD_OUT))
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    ns = _parse_args(argv)
    p = Path(ns.repro_csv)
    if not p.exists():
        raise SystemExit(f"[claim-density] missing {p}")
    with p.open(newline="") as fh:
        repro = list(csv.DictReader(fh))
    rows = _per_graph(repro)
    summary = _summarise(rows)
    _emit_csv(rows, Path(ns.csv_out))
    _emit_json(rows, summary, Path(ns.json_out))
    _emit_md(rows, summary, Path(ns.md_out))
    md_path = Path(ns.md_out).resolve()
    try:
        md_display = md_path.relative_to(REPO_ROOT)
    except ValueError:
        md_display = md_path
    print(
        f"[claim-density] graphs={summary['n_graphs']} "
        f"claims={summary['total_claims']} "
        f"ok={summary['total_ok']} ({summary['total_ok_pct']:.1f}%) → "
        f"{md_display}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
