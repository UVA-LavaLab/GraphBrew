#!/usr/bin/env python3
"""Per-kernel oracle-gap breakdown.

The family/regime breakdowns in ``oracle_gap.json`` answer "where
does each policy struggle?" but hide an orthogonal axis the paper
needs: per-graph-kernel performance. A reviewer who cares about
SSSP/PR/BC/CC/BFS as primitives wants to know, per kernel, which
policy is closest to the empirical oracle.

This report buckets the existing per-cell oracle-gap rows by
``(policy, app)`` and emits per-bucket {mean, median, p90, max,
n, wins}, plus a per-app winner ranking.

Inputs
------
* ``wiki/data/oracle_gap.json`` — per-cell oracle-gap rows.

Outputs
-------
* ``wiki/data/oracle_gap_by_app.{csv,json,md}``

The headline finding (as of 114-cell corpus): POPT dominates pr
(0.100 pp vs GRASP 2.251 pp); GRASP dominates cc (0.640 pp vs
POPT 3.572 pp); on sssp GRASP is the *worst* of the four
(7.106 pp vs POPT 1.713 pp). The policy-vs-kernel matrix is a
strong counter to any one-size-fits-all story.
"""

from __future__ import annotations

import argparse
import csv
import json
import statistics
from collections import defaultdict
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
WIKI = REPO_ROOT / "wiki" / "data"

POLICIES = ("LRU", "SRRIP", "GRASP", "POPT")


def _p90(vals: list[float]) -> float:
    if not vals:
        return 0.0
    sv = sorted(vals)
    idx = min(len(sv) - 1, max(0, int(0.90 * len(sv))))
    return sv[idx]


def _per_bucket(rows: list[dict]) -> dict:
    by_pa: dict[tuple[str, str], list[dict]] = defaultdict(list)
    for r in rows:
        p, a = r.get("policy"), r.get("app")
        if p not in POLICIES or not a:
            continue
        try:
            gap = float(r["gap_pp"])
        except (ValueError, KeyError, TypeError):
            continue
        by_pa[(p, a)].append({
            "graph": r.get("graph"),
            "family": r.get("family"),
            "l3_size": r.get("l3_size"),
            "regime": r.get("regime"),
            "gap_pp": gap,
            "miss_rate": float(r.get("miss_rate", 0)) if r.get("miss_rate") else 0.0,
            "is_winner": int(r.get("is_winner", 0)),
        })

    out: dict = {"by_policy_app": {}, "by_app_ranking": {}}
    apps = sorted({a for (_, a) in by_pa})

    for (p, a), entries in sorted(by_pa.items()):
        vals = [e["gap_pp"] for e in entries]
        out["by_policy_app"][f"{p}/{a}"] = {
            "n":      len(vals),
            "mean":   round(statistics.fmean(vals), 4),
            "median": round(statistics.median(vals), 4),
            "p90":    round(_p90(vals), 4),
            "max":    round(max(vals), 4),
            "wins":   sum(e["is_winner"] for e in entries),
        }

    for a in apps:
        ranking = []
        for p in POLICIES:
            b = out["by_policy_app"].get(f"{p}/{a}")
            if b:
                ranking.append({
                    "policy": p,
                    "mean_gap_pp": b["mean"],
                    "median_gap_pp": b["median"],
                    "p90_gap_pp": b["p90"],
                    "max_gap_pp": b["max"],
                    "n": b["n"],
                    "wins": b["wins"],
                })
        ranking.sort(key=lambda x: x["mean_gap_pp"])
        out["by_app_ranking"][a] = ranking

    return out


def _emit_csv(rows: list[dict], out: dict, csv_path: Path) -> None:
    csv_path.parent.mkdir(parents=True, exist_ok=True)
    fields = ["app", "policy", "n", "mean_gap_pp", "median_gap_pp",
              "p90_gap_pp", "max_gap_pp", "wins"]
    with csv_path.open("w", newline="") as fh:
        w = csv.DictWriter(fh, fieldnames=fields)
        w.writeheader()
        for app, ranking in sorted(out["by_app_ranking"].items()):
            for r in ranking:
                w.writerow({
                    "app": app,
                    "policy": r["policy"],
                    "n": r["n"],
                    "mean_gap_pp":   r["mean_gap_pp"],
                    "median_gap_pp": r["median_gap_pp"],
                    "p90_gap_pp":    r["p90_gap_pp"],
                    "max_gap_pp":    r["max_gap_pp"],
                    "wins": r["wins"],
                })


def _emit_md(out: dict, md_path: Path) -> None:
    lines: list[str] = []
    lines.append("# Per-kernel oracle gap\n")
    lines.append(
        "_Generated by `scripts/experiments/ecg/oracle_gap_by_app.py`. "
        "Buckets the per-cell oracle-gap rows by (policy, app); the per-app "
        "winner is the policy with the smallest mean gap to the cell-local "
        "empirical oracle.\n"
    )

    lines.append("## Per-app winner ranking\n")
    for app, ranking in sorted(out["by_app_ranking"].items()):
        lines.append(f"### `{app}`\n")
        lines.append("| rank | policy | n | mean gap (pp) | median | p90 | max | wins |")
        lines.append("|---:|---|---:|---:|---:|---:|---:|---:|")
        for i, r in enumerate(ranking, 1):
            marker = " 🥇" if i == 1 else ""
            lines.append(
                f"| {i}{marker} | `{r['policy']}` | {r['n']} | {r['mean_gap_pp']:.3f} | "
                f"{r['median_gap_pp']:.3f} | {r['p90_gap_pp']:.3f} | "
                f"{r['max_gap_pp']:.3f} | {r['wins']} |"
            )
        lines.append("")

    lines.append("## Per-(policy, app) matrix\n")
    apps = sorted(out["by_app_ranking"].keys())
    lines.append("| policy | " + " | ".join(f"`{a}`" for a in apps) + " |")
    lines.append("|---|" + "|".join(["---:"] * len(apps)) + "|")
    for p in POLICIES:
        cells = []
        for a in apps:
            b = out["by_policy_app"].get(f"{p}/{a}")
            cells.append(f"{b['mean']:.3f}" if b else "-")
        lines.append(f"| `{p}` | " + " | ".join(cells) + " |")

    md_path.parent.mkdir(parents=True, exist_ok=True)
    md_path.write_text("\n".join(lines) + "\n")


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--oracle-json", type=Path, default=WIKI / "oracle_gap.json")
    p.add_argument("--csv-out",     type=Path, default=WIKI / "oracle_gap_by_app.csv")
    p.add_argument("--json-out",    type=Path, default=WIKI / "oracle_gap_by_app.json")
    p.add_argument("--md-out",      type=Path, default=WIKI / "oracle_gap_by_app.md")
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    ns = _parse_args(argv)
    doc = json.loads(ns.oracle_json.read_text())
    rows = doc.get("rows", [])
    if not rows:
        raise SystemExit(
            f"[oracle-by-app] {ns.oracle_json} has no `rows` — run `make lit-oracle-gap`"
        )

    out = _per_bucket(rows)
    _emit_csv(rows, out, ns.csv_out)
    ns.json_out.parent.mkdir(parents=True, exist_ok=True)
    ns.json_out.write_text(json.dumps(out, indent=2, sort_keys=True) + "\n")
    _emit_md(out, ns.md_out)

    md_path = Path(ns.md_out).resolve()
    try:
        md_display = md_path.relative_to(REPO_ROOT)
    except ValueError:
        md_display = md_path
    print(
        f"[oracle-by-app] {len(out['by_policy_app'])} (policy, app) buckets; "
        f"{len(out['by_app_ranking'])} apps → {md_display}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
