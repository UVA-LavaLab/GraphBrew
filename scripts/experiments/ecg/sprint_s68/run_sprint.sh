#!/usr/bin/env bash
# run_sprint.sh — Sprint S68 driver.
#
# Runs the next ready milestone whose status file does not yet exist
# (or whose status is "blocked"/"fail" — re-runs are idempotent because
# every milestone script is idempotent).
#
# Usage:
#   bash scripts/experiments/ecg/sprint_s68/run_sprint.sh           # run next
#   bash scripts/experiments/ecg/sprint_s68/run_sprint.sh M1        # run specific
#   bash scripts/experiments/ecg/sprint_s68/run_sprint.sh --all     # run all in sequence
#   bash scripts/experiments/ecg/sprint_s68/run_sprint.sh --status  # show sprint state
#
# After each milestone finishes OK, the driver emits a structured
# rubber-duck report at:
#   ${RESULTS_DIR}/<m_id>/rubber_duck_request.md
#
# The caller (agent or human) is responsible for invoking the
# rubber-duck and recording the verdict at:
#   ${RESULTS_DIR}/<m_id>/rubber_duck_verdict.md
#
# A milestone is considered "gated" (and the next milestone will NOT
# run automatically) until its rubber_duck_verdict.md exists AND
# contains "GREEN" at the start.

set -euo pipefail
HERE="$(dirname "$(readlink -f "${BASH_SOURCE[0]}")")"
source "${HERE}/common.sh"

declare -A MILESTONES=(
  [M1]="s68-m1-mmu-patch:m1_mmu_patch.sh"
  [M1b]="s68-m1b-queue-servicing:m1b_queue_servicing.sh"
  [M2]="s68-m2-smoke-diff:m2_smoke_diff.sh"
  [M3]="s68-m3-multi-graph:m3_multi_graph.sh"
  [M4]="s68-m4-rebuild-riscv:m4_rebuild_riscv.sh"
  [M5]="s68-m5-isa-smoke:m5_isa_smoke.sh"
  [M6]="s68-m6-parity-check:m6_parity_check.sh"
  [M7]="s68-m7-verdict-doc:m7_verdict_doc.sh"
)

ORDER=(M1 M1b M2 M3 M4 M5 M6 M7)

status_of() {
  local m_id="$1"
  local s="${RESULTS_DIR}/${m_id}/status.json"
  if [ ! -f "${s}" ]; then echo "pending"; return; fi
  python3 -c "import json; print(json.load(open('${s}'))['status'])"
}

gate_of() {
  local m_id="$1"
  local v="${RESULTS_DIR}/${m_id}/rubber_duck_verdict.md"
  if [ ! -f "${v}" ]; then echo "no_verdict"; return; fi
  if head -1 "${v}" | grep -qi "^GREEN"; then echo "green"
  elif head -1 "${v}" | grep -qi "^YELLOW"; then echo "yellow"
  elif head -1 "${v}" | grep -qi "^RED"; then echo "red"
  else echo "unrecognized"
  fi
}

emit_rubber_duck_request() {
  local m_id="$1"
  local req="${RESULTS_DIR}/${m_id}/rubber_duck_request.md"
  local status_json="${RESULTS_DIR}/${m_id}/status.json"
  local log_file="${RESULTS_DIR}/${m_id}/log.txt"
  cat > "${req}" <<EOF
# Rubber-duck request — ${m_id}

**Auto-generated by sprint S68 driver on $(date -Is).**

## Milestone status (JSON)

\`\`\`json
$(cat "${status_json}" 2>/dev/null || echo '{ "error": "no status.json" }')
\`\`\`

## Tail of milestone log (last 80 lines)

\`\`\`
$(tail -80 "${log_file}" 2>/dev/null || echo '(no log)')
\`\`\`

## Rubber-duck instructions

Please review the above evidence and respond by writing to:
  ${RESULTS_DIR}/${m_id}/rubber_duck_verdict.md

The verdict file MUST start with one of:
  GREEN   — Milestone evidence holds up; safe to advance to next milestone
  YELLOW  — Concerns noted; remediation suggested but not blocking
  RED     — Evidence does not support the milestone exit criteria;
            STOP the sprint and surface to the user

Specifically scrutinize for:
- Are the exit-criteria numbers actually consistent with the source-code
  semantics (e.g., does pf_issued > 0 actually mean prefetches reached
  memory, or could it count rejected-in-MSHR-as-issued?)
- Does the evidence rule out the most likely alternative explanations?
- Is the baseline a fair comparison (same workload, same iterations,
  same cache config)?
- Are there any "too good to be true" numbers that warrant a sanity
  check (e.g., useful/issued ratio > 95%)?
EOF
  echo "${req}"
}

cmd_status() {
  printf '%-6s %-26s %-10s %-12s\n' "ID" "Todo" "Status" "RubberDuck"
  printf '%-6s %-26s %-10s %-12s\n' "──" "────" "──────" "──────────"
  for key in "${ORDER[@]}"; do
    IFS=':' read -r m_id _script <<< "${MILESTONES[$key]}"
    s="$(status_of "${m_id}")"
    g="$(gate_of "${m_id}")"
    printf '%-6s %-26s %-10s %-12s\n' "${key}" "${m_id}" "${s}" "${g}"
  done
}

run_one() {
  local key="$1"
  IFS=':' read -r m_id script <<< "${MILESTONES[$key]}"
  echo ""
  echo "════════════════════════════════════════════════════════════════"
  echo "  Sprint S68 → ${key}: ${m_id}"
  echo "════════════════════════════════════════════════════════════════"
  bash "${HERE}/${script}" || true
  local s="$(status_of "${m_id}")"
  if [ "${s}" = "ok" ]; then
    req="$(emit_rubber_duck_request "${m_id}")"
    echo ""
    echo "  ${m_id} → status=ok"
    echo "  Rubber-duck request: ${req}"
    echo "  Awaiting verdict at: ${RESULTS_DIR}/${m_id}/rubber_duck_verdict.md"
  else
    echo ""
    echo "  ${m_id} → status=${s} (BLOCKED — see status.json)"
    cat "${RESULTS_DIR}/${m_id}/status.json"
  fi
}

cmd_next() {
  for key in "${ORDER[@]}"; do
    IFS=':' read -r m_id _ <<< "${MILESTONES[$key]}"
    s="$(status_of "${m_id}")"
    if [ "${s}" = "pending" ]; then
      run_one "${key}"
      return
    fi
    if [ "${s}" = "ok" ]; then
      g="$(gate_of "${m_id}")"
      if [ "${g}" = "no_verdict" ]; then
        echo "Milestone ${key} (${m_id}) is OK but awaiting rubber-duck verdict."
        echo "Request: ${RESULTS_DIR}/${m_id}/rubber_duck_request.md"
        return
      fi
      if [ "${g}" = "red" ]; then
        echo "Milestone ${key} (${m_id}) rubber-duck flagged RED — sprint halted."
        cat "${RESULTS_DIR}/${m_id}/rubber_duck_verdict.md"
        return
      fi
      # green or yellow — proceed
      continue
    fi
    if [ "${s}" = "blocked" ] || [ "${s}" = "fail" ]; then
      echo "Milestone ${key} (${m_id}) is ${s}. Inspect status.json + log.txt before retrying:"
      cat "${RESULTS_DIR}/${m_id}/status.json"
      return
    fi
  done
  echo "All sprint S68 milestones complete!"
}

cmd_all() {
  for key in "${ORDER[@]}"; do
    IFS=':' read -r m_id _ <<< "${MILESTONES[$key]}"
    s="$(status_of "${m_id}")"
    if [ "${s}" = "ok" ]; then
      g="$(gate_of "${m_id}")"
      if [ "${g}" = "red" ]; then
        echo "STOP: ${key} rubber-duck=RED"
        return 1
      fi
      if [ "${g}" = "no_verdict" ]; then
        echo "STOP: ${key} awaiting rubber-duck verdict (request file at ${RESULTS_DIR}/${m_id}/rubber_duck_request.md)"
        return 1
      fi
      continue
    fi
    run_one "${key}"
    s="$(status_of "${m_id}")"
    if [ "${s}" != "ok" ]; then
      echo "STOP: ${key} status=${s}"
      return 1
    fi
    echo "STOP: ${key} OK, awaiting rubber-duck verdict before advancing"
    return 0
  done
}

case "${1:-next}" in
  --status|status) cmd_status ;;
  --all|all)       cmd_all ;;
  --next|next)     cmd_next ;;
  M[1-7]|M1b)      run_one "$1" ;;
  *) echo "usage: $0 [--status|--next|--all|M1|M1b|M2..M7]"; exit 2 ;;
esac
