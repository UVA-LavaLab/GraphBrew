"""Literature-faithfulness accesses-floor audit (LIT-Acc, gate 227).

For every (policy, graph, app, l3_size) row emitted by the lit-faith
comparator, the `accesses` field is the total number of LLC accesses
the cache_sim trace consumed when measuring `policy_miss_rate`. Rows
with very low access counts are dominated by warmup noise: a single
cache-warming pass can swing the miss-rate by several pp, which would
make even a perfectly-faithful comparator look like it disagrees with
the literature.

This gate audits the access distribution of the lit-faith corpus
*without* re-running any simulations. It enforces:

  * a hard floor per (graph, app) on "production" graphs — anything
    silently degrading to a warmup-only trace will fail;
  * a separate, looser floor for the `email-Eu-core` dev-smoke graph
    (the corpus's intentional tiny target);
  * per-axis distribution surfacing (graph / app / l3 / policy);
  * warmup-bucket classification (`tiny / small / medium / large / huge`)
    so the dashboard can tell at a glance how heavy the workload mix is.

The generator does not touch live simulator state — it reads the
`per_claim` table of `literature_faithfulness_postfix.json`, which is
itself regenerated by `make lit-faith`.
"""

from __future__ import annotations

import argparse
import csv
import json
import math
import statistics
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Dict, Iterable, List, Tuple

REPO_ROOT = Path(__file__).resolve().parents[3]

SMOKE_GRAPHS = {"email-Eu-core"}

# Per-app floors enforced ONLY for production graphs (non-smoke). These
# floors are intentionally about an order of magnitude below today's
# empirical minimums so that a real warmup-truncation regression (often
# a 10-100x drop in accesses) trips immediately, without flagging the
# corpus's natural per-app variation.
PRODUCTION_PER_APP_FLOOR: Dict[str, int] = {
    "bc":   1_000_000,
    "bfs":    500_000,
    "cc":   1_000_000,
    "pr":   1_000_000,
    "sssp": 500_000,
}

# Per-app floors for `email-Eu-core` only. Single-thread deterministic
# cache_sim reports L3-layer accesses; email-Eu-core's working set fits
# low in the hierarchy, so L3 may see only a few hundred cold-fill lines.
# Floor at 300 keeps a true zero/degenerate trace failing while accepting
# the honest power-law smoke behavior.
SMOKE_PER_APP_FLOOR: Dict[str, int] = {
    "bc":        300,
    "bfs":       300,
    "pr":        300,
    "cc":        300,
    "sssp":      300,
}

# Bucket boundaries (right-exclusive, except `huge` is right-unbounded).
BUCKET_EDGES: List[Tuple[str, int]] = [
    ("tiny",   100_000),
    ("small", 1_000_000),
    ("medium", 10_000_000),
    ("large",  100_000_000),
    ("huge",   None),
]


def _bucket(accesses: int) -> str:
    for name, ceil in BUCKET_EDGES:
        if ceil is None:
            return name
        if accesses < ceil:
            return name
    return "huge"


def _pct(numerator: int, denominator: int) -> float:
    return 100.0 * numerator / denominator if denominator else 0.0


def _quantile(values: List[int], q: float) -> int:
    if not values:
        return 0
    s = sorted(values)
    idx = max(0, min(len(s) - 1, int(round((len(s) - 1) * q))))
    return s[idx]


def _summarize(values: List[int]) -> Dict[str, Any]:
    if not values:
        return {"n": 0}
    return {
        "n":      len(values),
        "min":    int(min(values)),
        "p10":    _quantile(values, 0.10),
        "median": int(statistics.median(values)),
        "p90":    _quantile(values, 0.90),
        "max":    int(max(values)),
        "mean":   int(round(statistics.mean(values))),
        "log10_median": round(math.log10(statistics.median(values)), 2) if statistics.median(values) > 0 else None,
    }


def _violations(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    for r in rows:
        graph = r["graph"]
        app = r["app"]
        acc = int(r["accesses"])
        smoke = graph in SMOKE_GRAPHS
        floor = (SMOKE_PER_APP_FLOOR if smoke else PRODUCTION_PER_APP_FLOOR).get(app)
        if floor is None:
            # Unknown app -- conservatively require a non-trivial trace
            floor = 100_000
        if acc < floor:
            out.append({
                "graph":    graph,
                "app":      app,
                "policy":   r["policy"],
                "l3_size":  r["l3_size"],
                "accesses": acc,
                "floor":    floor,
                "smoke":    smoke,
                "shortfall": floor - acc,
                "shortfall_pct": round(_pct(floor - acc, floor), 2),
            })
    out.sort(key=lambda v: v["shortfall_pct"], reverse=True)
    return out


def build_audit(lit_faith: Dict[str, Any]) -> Dict[str, Any]:
    rows: List[Dict[str, Any]] = list(lit_faith.get("per_claim") or [])
    if not rows:
        raise SystemExit("[lit-faith-accesses] empty per_claim table — run `make lit-faith` first")

    per_row: List[Dict[str, Any]] = []
    by_graph: Dict[str, List[int]] = defaultdict(list)
    by_app:   Dict[str, List[int]] = defaultdict(list)
    by_l3:    Dict[str, List[int]] = defaultdict(list)
    by_policy: Dict[str, List[int]] = defaultdict(list)
    by_graph_app: Dict[Tuple[str, str], List[int]] = defaultdict(list)
    bucket_counts: Dict[str, int] = defaultdict(int)
    bucket_counts_production: Dict[str, int] = defaultdict(int)

    for r in rows:
        acc = int(r["accesses"])
        graph = r["graph"]
        app = r["app"]
        l3 = r["l3_size"]
        pol = r["policy"]
        bucket = _bucket(acc)
        smoke = graph in SMOKE_GRAPHS

        per_row.append({
            "graph":     graph,
            "app":       app,
            "policy":    pol,
            "l3_size":   l3,
            "accesses":  acc,
            "log10":     round(math.log10(acc), 3) if acc > 0 else None,
            "bucket":    bucket,
            "smoke":     smoke,
        })

        by_graph[graph].append(acc)
        by_app[app].append(acc)
        by_l3[l3].append(acc)
        by_policy[pol].append(acc)
        by_graph_app[(graph, app)].append(acc)
        bucket_counts[bucket] += 1
        if not smoke:
            bucket_counts_production[bucket] += 1

    violations = _violations(rows)
    production_rows = [r for r in rows if r["graph"] not in SMOKE_GRAPHS]
    smoke_rows = [r for r in rows if r["graph"] in SMOKE_GRAPHS]

    summary = {
        "total_rows":       len(rows),
        "production_rows":  len(production_rows),
        "smoke_rows":       len(smoke_rows),
        "graph_count":      len(by_graph),
        "app_count":        len(by_app),
        "l3_count":         len(by_l3),
        "policy_count":     len(by_policy),
        "violation_count":  len(violations),
        "production_min_accesses":      min(int(r["accesses"]) for r in production_rows) if production_rows else 0,
        "production_median_accesses":   int(statistics.median([int(r["accesses"]) for r in production_rows])) if production_rows else 0,
        "smoke_min_accesses":           min(int(r["accesses"]) for r in smoke_rows) if smoke_rows else 0,
        "smoke_median_accesses":        int(statistics.median([int(r["accesses"]) for r in smoke_rows])) if smoke_rows else 0,
        "buckets_all":         dict(bucket_counts),
        "buckets_production":  dict(bucket_counts_production),
    }

    audit = {
        "schema_version": 1,
        "summary":        summary,
        "floors": {
            "production_per_app": dict(PRODUCTION_PER_APP_FLOOR),
            "smoke_per_app":      dict(SMOKE_PER_APP_FLOOR),
            "smoke_graphs":       sorted(SMOKE_GRAPHS),
        },
        "buckets": [
            {"name": name, "upper_exclusive": ceil}
            for name, ceil in BUCKET_EDGES
        ],
        "per_graph": {g: _summarize(v) for g, v in sorted(by_graph.items())},
        "per_app":   {a: _summarize(v) for a, v in sorted(by_app.items())},
        "per_l3":    {l: _summarize(v) for l, v in sorted(by_l3.items())},
        "per_policy": {p: _summarize(v) for p, v in sorted(by_policy.items())},
        "per_graph_app": {
            f"{g}::{a}": _summarize(v)
            for (g, a), v in sorted(by_graph_app.items())
        },
        "violations": violations,
        "per_row": per_row,
    }
    return audit


def write_markdown(audit: Dict[str, Any], path: Path) -> None:
    s = audit["summary"]
    lines: List[str] = []
    lines.append("# Literature-faithfulness accesses-floor audit\n")
    lines.append("Generated by `make lit-accesses`. Floors on `accesses` per "
                 "(graph, app) detect warmup-noise contamination in the lit-faith corpus.\n")
    lines.append("## Summary\n")
    lines.append(f"- Total rows: **{s['total_rows']}** "
                 f"(production: {s['production_rows']}, smoke: {s['smoke_rows']})")
    lines.append(f"- Graphs / apps / L3 / policies: "
                 f"{s['graph_count']} / {s['app_count']} / {s['l3_count']} / {s['policy_count']}")
    lines.append(f"- Production min accesses: **{s['production_min_accesses']:,}**"
                 f" (median {s['production_median_accesses']:,})")
    lines.append(f"- Smoke (email-Eu-core) min accesses: **{s['smoke_min_accesses']:,}**"
                 f" (median {s['smoke_median_accesses']:,})")
    lines.append(f"- Violations vs floor: **{s['violation_count']}**\n")

    lines.append("## Warmup buckets (production rows)\n")
    lines.append("| bucket | count | share |")
    lines.append("|--------|------:|------:|")
    total_prod = s["production_rows"] or 1
    for name, _ in BUCKET_EDGES:
        c = s["buckets_production"].get(name, 0)
        lines.append(f"| {name} | {c} | {_pct(c, total_prod):.1f}% |")
    lines.append("")

    lines.append("## Per-graph accesses\n")
    lines.append("| graph | n | min | p10 | median | p90 | max |")
    lines.append("|-------|--:|----:|----:|------:|----:|----:|")
    for g, stat in audit["per_graph"].items():
        lines.append(f"| {g} | {stat['n']} | {stat['min']:,} | {stat['p10']:,} | "
                     f"{stat['median']:,} | {stat['p90']:,} | {stat['max']:,} |")
    lines.append("")

    lines.append("## Per-app accesses\n")
    lines.append("| app | n | min | median | max |")
    lines.append("|-----|--:|----:|------:|----:|")
    for a, stat in audit["per_app"].items():
        lines.append(f"| {a} | {stat['n']} | {stat['min']:,} | {stat['median']:,} | {stat['max']:,} |")
    lines.append("")

    if audit["violations"]:
        lines.append("## Floor violations\n")
        lines.append("| graph | app | policy | l3 | accesses | floor | shortfall |")
        lines.append("|-------|-----|--------|----|---------:|------:|----------:|")
        for v in audit["violations"]:
            lines.append(f"| {v['graph']} | {v['app']} | {v['policy']} | "
                         f"{v['l3_size']} | {v['accesses']:,} | {v['floor']:,} | "
                         f"{v['shortfall_pct']:.1f}% |")
        lines.append("")
    else:
        lines.append("_No floor violations: every (graph, app) row meets its accesses floor._")

    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_csv(audit: Dict[str, Any], path: Path) -> None:
    with path.open("w", newline="", encoding="utf-8") as fh:
        w = csv.DictWriter(fh, fieldnames=["graph", "app", "policy", "l3_size",
                                            "accesses", "log10", "bucket", "smoke"])
        w.writeheader()
        for row in audit["per_row"]:
            w.writerow(row)


def main(argv: Iterable[str] | None = None) -> int:
    p = argparse.ArgumentParser(description="LIT-Acc accesses-floor audit (gate 227)")
    p.add_argument("--lit-faith-json", type=Path,
                   default=REPO_ROOT / "wiki" / "data" / "literature_faithfulness_postfix.json")
    p.add_argument("--json-out", type=Path,
                   default=REPO_ROOT / "wiki" / "data" / "lit_faith_accesses.json")
    p.add_argument("--md-out", type=Path,
                   default=REPO_ROOT / "wiki" / "data" / "lit_faith_accesses.md")
    p.add_argument("--csv-out", type=Path,
                   default=REPO_ROOT / "wiki" / "data" / "lit_faith_accesses.csv")
    args = p.parse_args(list(argv) if argv is not None else None)

    if not args.lit_faith_json.exists():
        print(f"[lit-faith-accesses] missing {args.lit_faith_json}; run `make lit-faith` first",
              file=sys.stderr)
        return 1

    lit_faith = json.loads(args.lit_faith_json.read_text(encoding="utf-8"))
    audit = build_audit(lit_faith)

    args.json_out.parent.mkdir(parents=True, exist_ok=True)
    args.json_out.write_text(json.dumps(audit, indent=2, sort_keys=True) + "\n",
                              encoding="utf-8")
    write_markdown(audit, args.md_out)
    write_csv(audit, args.csv_out)

    s = audit["summary"]
    print(f"[lit-faith-accesses] {s['total_rows']} rows; production min {s['production_min_accesses']:,}, "
          f"median {s['production_median_accesses']:,}; violations {s['violation_count']}.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
