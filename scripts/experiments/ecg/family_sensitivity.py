#!/usr/bin/env python3
"""Family-classification sensitivity sweep.

A reviewer is entitled to ask: how much do our headline sign claims
depend on the specific family label we attached to each graph? The
canonical map is

    cit-Patents     -> citation
    com-orkut       -> social
    delaunay_n19    -> mesh
    email-Eu-core   -> social
    roadNet-CA      -> road
    soc-LiveJournal1-> social
    soc-pokec       -> social
    web-Google      -> web

This script enumerates every (graph, alternative_family) reassignment
(8 graphs × 4 alternative families = 32 perturbations), re-runs the
seven sign-stability claims used by `bootstrap_ci.py`, and reports
which perturbations would flip ANY claim across the 0.95 stability
floor.

A "robust" sign claim survives every relabeling; a "fragile" claim
relies on a specific graph having a specific family. The output is
read by `paper_claims_registry.py` so the paper can quote the
sensitivity right next to each headline.

Inputs:  wiki/data/oracle_gap.json  (per-cell rows with `family`)
Outputs: wiki/data/family_sensitivity.{json,md}
"""

from __future__ import annotations

import argparse
import json
import random
import statistics
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
WIKI = REPO_ROOT / "wiki" / "data"

DEFAULT_ORACLE_JSON = WIKI / "oracle_gap.json"
DEFAULT_JSON_OUT = WIKI / "family_sensitivity.json"
DEFAULT_MD_OUT = WIKI / "family_sensitivity.md"

CANONICAL_FAMILY = {
    "cit-Patents":     "citation",
    "com-orkut":       "social",
    "delaunay_n19":    "mesh",
    "email-Eu-core":   "social",
    "roadNet-CA":      "road",
    "soc-LiveJournal1": "social",
    "soc-pokec":       "social",
    "web-Google":      "web",
}

ALL_FAMILIES = ("citation", "mesh", "road", "social", "web")

# Seven sign claims, mirroring bootstrap_ci.py's set.  Pairs `(a, b, family)`
# encode the claim "mean(a, family) < mean(b, family)".
SIGN_CLAIMS = [
    ("POPT", "GRASP", "road"),
    ("POPT", "GRASP", "social"),
    ("POPT", "GRASP", "mesh"),
    ("POPT", "GRASP", "citation"),
    ("POPT", "GRASP", "web"),
    ("POPT", "LRU",   "social"),
    ("GRASP", "LRU",  "social"),
]

# A claim is "stable" iff resampled fraction(a<b) >= STABILITY_FLOOR.
STABILITY_FLOOR = 0.95

DEFAULT_N_RESAMPLES = 2000     # 2k is plenty for sign-stability sweeps
DEFAULT_SEED = 1729            # match bootstrap_ci default


def _sign_stability(rows, pol_a, pol_b, family, n_resamples, rng):
    a_vals = [
        float(r["gap_pp"]) for r in rows
        if r.get("policy") == pol_a and r.get("family") == family
    ]
    b_vals = [
        float(r["gap_pp"]) for r in rows
        if r.get("policy") == pol_b and r.get("family") == family
    ]
    if not a_vals or not b_vals:
        return {"frac": None, "n_a": len(a_vals), "n_b": len(b_vals)}
    n_lt = 0
    for _ in range(n_resamples):
        a_mean = sum(rng.choice(a_vals) for _ in range(len(a_vals))) / len(a_vals)
        b_mean = sum(rng.choice(b_vals) for _ in range(len(b_vals))) / len(b_vals)
        if a_mean < b_mean:
            n_lt += 1
    return {
        "frac":     round(n_lt / n_resamples, 4),
        "mean_a":   round(statistics.fmean(a_vals), 4),
        "mean_b":   round(statistics.fmean(b_vals), 4),
        "n_a":      len(a_vals),
        "n_b":      len(b_vals),
    }


def _evaluate_claims(rows, n_resamples, seed):
    rng = random.Random(seed)
    results = []
    for a, b, fam in SIGN_CLAIMS:
        st = _sign_stability(rows, a, b, fam, n_resamples, rng)
        results.append({
            "claim":  f"{a} < {b} on {fam}",
            "family": fam,
            "policy_a": a, "policy_b": b,
            **st,
        })
    return results


def _apply_reassignment(rows, graph, new_family):
    """Return new rows where every row.graph == graph has family overridden."""
    out = []
    for r in rows:
        if r.get("graph") == graph:
            cp = dict(r)
            cp["family"] = new_family
            out.append(cp)
        else:
            out.append(r)
    return out


def _flips(canonical, perturbed):
    """List sign claims whose stability state crossed the floor."""
    flipped = []
    for c, p in zip(canonical, perturbed):
        canonical_stable = c["frac"] is not None and c["frac"] >= STABILITY_FLOOR
        perturbed_stable = p["frac"] is not None and p["frac"] >= STABILITY_FLOOR
        if canonical_stable != perturbed_stable:
            flipped.append({
                "claim":          c["claim"],
                "frac_canonical": c["frac"],
                "frac_perturbed": p["frac"],
                "direction":      "lost" if canonical_stable else "gained",
            })
    return flipped


def _fmt_frac(x):
    return f"{x:.3f}" if x is not None else "n/a"


def _emit_md(doc, path: Path):
    lines = [
        "# Family-classification sensitivity",
        "",
        "_Generated by `scripts/experiments/ecg/family_sensitivity.py`. "
        "Enumerates every (graph, alternative_family) relabeling, re-runs the "
        f"seven sign-stability claims used by `bootstrap_ci.py`, and reports "
        f"flips across the {STABILITY_FLOOR:.2f} stability floor._",
        "",
        f"**Verdict:** "
        f"{'✅ robust' if doc['n_flipping_relabelings'] == 0 else '⛔ FRAGILE'} "
        f"({doc['n_flipping_relabelings']} / {doc['n_relabelings']} "
        f"perturbations flip ≥1 claim)",
        "",
        "## Canonical claim stability (baseline)",
        "",
        "| claim | n_a | n_b | frac(a<b) | stable @ 0.95? |",
        "|---|---:|---:|---:|:---:|",
    ]
    for c in doc["canonical_claims"]:
        stable = "✅" if c["frac"] is not None and c["frac"] >= STABILITY_FLOOR else "—"
        frac_s = f"{c['frac']:.4f}" if c["frac"] is not None else "n/a"
        lines.append(f"| `{c['claim']}` | {c['n_a']} | {c['n_b']} | {frac_s} | {stable} |")

    lines += [
        "",
        "## Per-relabeling impact",
        "",
        "Each row reassigns one graph to a non-canonical family and lists which "
        "of the seven sign claims gained or lost stability.",
        "",
        "| graph | canonical → new | flipped claims | n |",
        "|---|---|---|---:|",
    ]
    for row in doc["relabelings"]:
        flips_s = (
            ", ".join(
                f"`{f['claim']}` ({f['direction']}: "
                f"{_fmt_frac(f['frac_canonical'])}→{_fmt_frac(f['frac_perturbed'])})"
                for f in row["flipped"]
            )
            or "—"
        )
        lines.append(
            f"| `{row['graph']}` | `{row['canonical_family']}` → "
            f"`{row['new_family']}` | {flips_s} | {len(row['flipped'])} |"
        )

    lines += [
        "",
        "## Per-claim robustness",
        "",
        "| claim | canonical stable? | # relabelings that flip it |",
        "|---|:---:|---:|",
    ]
    for c, n in doc["per_claim_flip_count"].items():
        stable = next(x for x in doc["canonical_claims"] if x["claim"] == c)
        ok = "✅" if stable["frac"] is not None and stable["frac"] >= STABILITY_FLOOR else "—"
        lines.append(f"| `{c}` | {ok} | {n} |")
    lines.append("")
    path.write_text("\n".join(lines))


def main(argv=None):
    ap = argparse.ArgumentParser()
    ap.add_argument("--oracle-json", type=Path, default=DEFAULT_ORACLE_JSON)
    ap.add_argument("--json-out",    type=Path, default=DEFAULT_JSON_OUT)
    ap.add_argument("--md-out",      type=Path, default=DEFAULT_MD_OUT)
    ap.add_argument("--n-resamples", type=int, default=DEFAULT_N_RESAMPLES)
    ap.add_argument("--seed",        type=int, default=DEFAULT_SEED)
    ns = ap.parse_args(argv)

    rows = json.loads(ns.oracle_json.read_text()).get("rows", [])
    if not rows:
        raise SystemExit(
            f"[fam-sens] {ns.oracle_json} has no `rows` — run `make lit-oracle-gap`"
        )

    canonical_claims = _evaluate_claims(rows, ns.n_resamples, ns.seed)
    canonical_state = {c["claim"]: c["frac"] for c in canonical_claims}

    relabelings = []
    per_claim_flip = {c["claim"]: 0 for c in canonical_claims}

    for graph, canonical_family in CANONICAL_FAMILY.items():
        for new_family in ALL_FAMILIES:
            if new_family == canonical_family:
                continue
            perturbed_rows = _apply_reassignment(rows, graph, new_family)
            perturbed_claims = _evaluate_claims(perturbed_rows, ns.n_resamples, ns.seed)
            flipped = _flips(canonical_claims, perturbed_claims)
            for f in flipped:
                per_claim_flip[f["claim"]] += 1
            relabelings.append({
                "graph":             graph,
                "canonical_family":  canonical_family,
                "new_family":        new_family,
                "flipped":           flipped,
            })

    n_flipping = sum(1 for r in relabelings if r["flipped"])

    doc = {
        "meta": {
            "n_resamples":    ns.n_resamples,
            "seed":           ns.seed,
            "stability_floor": STABILITY_FLOOR,
        },
        "canonical_claims":         canonical_claims,
        "canonical_state":          canonical_state,
        "relabelings":              relabelings,
        "per_claim_flip_count":     per_claim_flip,
        "n_relabelings":            len(relabelings),
        "n_flipping_relabelings":   n_flipping,
    }

    ns.json_out.parent.mkdir(parents=True, exist_ok=True)
    ns.json_out.write_text(json.dumps(doc, indent=2, sort_keys=True) + "\n")
    _emit_md(doc, ns.md_out)

    md_path = Path(ns.md_out).resolve()
    try:
        md_display = md_path.relative_to(REPO_ROOT)
    except ValueError:
        md_display = md_path
    print(
        f"[fam-sens] {len(relabelings)} relabelings; "
        f"{n_flipping} flip ≥1 claim → {md_display}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
