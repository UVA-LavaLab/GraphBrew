"""Reproducibility smoke: did the committed wiki/data artifacts get
regenerated by a clean `make lit-claims lit-catalog` run?

Strategy:
  1. Snapshot every committed `wiki/data/**.{json,md}` file's SHA-256.
  2. Re-run the deterministic generator chain via Make.
  3. Re-hash and report any drift.

Drift is allowed only in well-known volatile fields:
  - confidence_dashboard.json: `suites[].runtime_s`, `suites[].raw_tail`
  - confidence_dashboard.md:   per-suite runtime cell
The set of paths to check, and the set of allowed volatile keys, are
declared up front so future generators must explicitly opt-in to
volatility (preserving the "all aggregators are bit-deterministic"
contract).

Outputs:
  wiki/data/reproduce_smoke.json  — per-file digest pair and verdict
  wiki/data/reproduce_smoke.md    — human-readable report
"""

from __future__ import annotations

import argparse
import copy
import hashlib
import json
import re
import subprocess
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
WIKI_DATA = REPO_ROOT / "wiki" / "data"

# Aggregator artifacts the smoke test will check.  Every file MUST be
# regenerable by `make lit-claims lit-catalog` (or its transitive deps)
# from inputs already on disk (the /tmp/graphbrew-lit-baseline JSONs
# plus the literature_baselines.py spec).  Add to this list whenever a
# new aggregator lands.
